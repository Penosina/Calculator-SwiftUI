import SwiftUI

extension Font {
    static func montserrat(size: CGFloat) -> Font {
        Font.custom("Montserrat-Bold", size: size)
    }
    
    static func museo(size: CGFloat) -> Font {
        Font.custom("Museo-700", size: size)
    }
    
    static func digitalNumbers(size: CGFloat) -> Font {
        Font.custom("DigitalNumbers-Regular", size: size)
    }
}
